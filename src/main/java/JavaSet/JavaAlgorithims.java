/*
The Java collections framework provides various algorithms
that can be used to manipulate elements stored in data structures.

Algorithms in Java are static methods that can be used to perform
various operations on collections.

Since algorithms can be used on various collections,
these are also known as generic algorithms.

Routine Data Manipulation
reverse() - reverses the order of elements

fill() - replace every element in a collection with the specified value

copy() - creates a copy of elements from the specified source to destination

swap() - swaps the position of two elements in a collection

addAll() - adds all the elements of a collection to other collection

*/