/*
The ListIterator interface of the Java collections
framework provides the functionality to access elements of a list.

It is bidirectional. This means it allows us to iterate elements of
a list in both the direction.

Methods of ListIterator

hasNext() - returns true if there exists an element in the list

next() - returns the next element of the list

nextIndex() returns the index of the element that the next()
method will return
previous() - returns the previous element of the list

previousIndex() - returns the index of the element that the previous()
method will return
remove() - removes the element returned by either next() or previous()

set() - replaces the element returned by either next() or previous()
with the specified element

*/
