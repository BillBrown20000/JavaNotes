/*
Like InputStreams OutputStream is an abstract class,
it is not useful by itself. However, its subclasses can be used to write data.

Subclasses of OutputStream
FileOutputStream
ByteArrayOutputStream
ObjectOutputStream

Methods of OutputStream
write() - writes the specified byte to the output stream

write(byte[] array) - writes the bytes from the specified
array to the output stream

flush() - forces to write all data present in output
stream to the destination

close() - closes the output stream

 */