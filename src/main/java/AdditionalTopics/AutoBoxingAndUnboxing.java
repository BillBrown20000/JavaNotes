/*
ava Autoboxing - Primitive Type to Wrapper Object
In autoboxing, the Java compiler automatically converts primitive types into
their corresponding wrapper class objects.

Java Unboxing - Wrapper Objects to Primitive Types
In unboxing, the Java compiler automatically converts wrapper class objects
into their corresponding primitive types.

Like autoboxing, unboxing can also be used with Java collections.


 */