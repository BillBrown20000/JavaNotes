/*
In Java, we use the exception handler components try, catch and
finally blocks to handle exceptions.

To catch and handle an exception, we place the try...catch...finally
block around the code that might generate an exception.
The finally block is optional.

The syntax for try...catch...finally is:

try {
  // code
} catch (ExceptionType e) {
  // catch block
} finally {
  // finally block
}

For each try block, there can be zero or more catch blocks.

The argument type of each catch block indicates the type of
exception that can be handled by it.
multiple catch blocks allow us to handle each exception differently.
 */
