package JavaFlowControl;

public class IfThen {
    /* if then expression is a boolean expression.
    A boolean expression returns either true or false.
     */
    public static void main(String[] args) {
        int yo = 13;
        int son = 7;
        if (yo > son){
            System.out.println("Yo Son");
        }
        /*if the expression is evaluated to true,
        statement(s) inside the body of if (statements inside parenthesis)
        are executed
         */
        if (son > yo){
            System.out.println("Yoooo son");
        }
        /*if the expression is evaluated to false, statement(s)
        inside the body of if are skipped from execution
         */
    }




}
